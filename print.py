a=10
b=-5
c=0
print(a)  #변수 a의 값 출력
print(b)  #변수 b의 값 출력
print(c)  #변수 c의 값 출력

print("a","b","c",sep="k")  #sep는 a,b,c 사이에 특정한 문자를 끼워넣는 것을 의미한다.

print("He is {}".format("Bob"))  #중괄호는 format안의 문자나 숫자를 출력한다
print("Today is {0}, sep {1}".format("Monday","12"))  #중괄호 안의 순서를 입력하면 format안의 해당하는 문자열이나 숫자가 출력된다.

